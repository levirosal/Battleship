package com.levi.model;

public class Player {
    private String name;
    private String locFile;

    public void setName(String name) {
        this.name = name;
    }

    public void setLocFile(String locFile) {
        this.locFile = locFile;
    }

    public String getName() {
        return name;
    }

    public String getLocFile() {
        return locFile;
    }

}
